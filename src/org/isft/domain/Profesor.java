package org.isft.domain;

/**
 *
 * @author st
 */
public class Profesor {
    private int legajoProfesor;
    private String nombre;
    private String apellido;
    private String direccion;
    private String localidad;
    private String cp;

    /**
     * @return the legajoProfesor
     */
    public int getLegajoProfesor() {
        return legajoProfesor;
    }

    /**
     * @param legajoProfesor the legajoProfesor to set
     */
    public void setLegajoProfesor(int legajoProfesor) {
        this.legajoProfesor = legajoProfesor;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the apellido
     */
    public String getApellido() {
        return apellido;
    }

    /**
     * @param apellido the apellido to set
     */
    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    /**
     * @return the direccion
     */
    public String getDireccion() {
        return direccion;
    }

    /**
     * @param direccion the direccion to set
     */
    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    /**
     * @return the localidad
     */
    public String getLocalidad() {
        return localidad;
    }

    /**
     * @param localidad the localidad to set
     */
    public void setLocalidad(String localidad) {
        this.localidad = localidad;
    }

    /**
     * @return the cp
     */
    public String getCp() {
        return cp;
    }

    /**
     * @param cp the cp to set
     */
    public void setCp(String cp) {
        this.cp = cp;
    }
    
    
}
