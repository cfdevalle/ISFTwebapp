
package org.isft.domain;


public class Ultimo_leg {
private int id;
private int lectivo;
private int ultlg;
private String clave;
private int cantregporacta;

    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getLectivo() {
        return lectivo;
    }

    public void setLectivo(int lectivo) {
        this.lectivo = lectivo;
    }

    public int getUltlg() {
        return ultlg;
    }

    public void setUltlg(int ultlg) {
        this.ultlg = ultlg;
    }

    public String getClave() {
        return clave;
    }

    public void setClave(String clave) {
        this.clave = clave;
    }
    
    public int getCantregporacta() {
        return cantregporacta;
    }

    public void setCant_reg_por_acta(int cant_reg_por_acta) {
        this.cantregporacta = cant_reg_por_acta;
    }

}
